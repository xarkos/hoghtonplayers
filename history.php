             <h2>How it all began...</h2><br>
 
                <p>The Hoghton Players G & S Society is the only G & S society in Southport, Formby and West Lancs. It was formed in 1952 as a church society based at the old Baptist Church down Hoghton Street, Southport. The aim was to produce some sort of outlet for the young people in the church to enable them to discover more about their skills and talents, whether it be singing, drama, dancing, scene painting, costume design etc. Over 50 years on, a handful of the original members are still involved to a greater or lesser extent in the Society.</p>
 
