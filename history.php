                 </div>

                <p>In 1961, the Hoghton Players transferred their annual productions to the Little Theatre (home of Southport Dramatic Club) also down Hoghton Street - where they remained until 1984. The following year 1985, saw the Society's first ever production at the Arts Centre, Lord Street, and they have remained there ever since.</p>
 
                <p>Although limited to a rather narrow genre, the Hoghton Players has always striven to break new ground - much in the same way as the original team of W S Gilbert & Sir Arthur Sullivan. As well as performing the most popular operas in the Savoy repertoire, the Hoghton has also performed some of the lesser known operas eg "Princess Ida" (1990 and 1999), "Utopia Ltd" (renamed "Utopia plc" in 1993) and a semi-staged version of "Cox and Box" in 1995.</p>

