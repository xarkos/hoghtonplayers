                     <h4>Northwest NODA Review - 16th March 2012</h4>
<p>Since the performance restrictions on G & S were lifted, many companies have embraced the chance to produce their Shows in many different styles and updated versions, to give new audiences a glimpse of their expertise and diversity.</p>
<p>Hoghton Players certainly managed to show that they are capable of presenting a most inventive, updated, captivating and humorous performance.</p>
<p>"Patience" Originally opened way back in the 1880’s and considered to contain much comedy and humor of the era but nothing would compare surely to that which this Company has managed to ingeniously mould, into a new fresh and suitably modified presentation</p>
<p>Up-dated to the 1970’s when the Hippy’s were "Hip" and Glam Rock was "Glam"</p>
<p>Satire was still there of course but with the clever changes of some of the Dialogue and Lyrics it turned the old age comedy into a new age success!!</p>
<p>Firstly I must Congratulate Ian Mackley (Director) for his superb adaptation and excellent Direction whilst completely overhauling the overall look, feel, sound, and style of this show, he still managed to preserve the great G & S at its best.</p>
<p>From the moment the show began, I knew that it was going to be a memorable evening and I can quite honestly say that I am now converted into being a G & S fan!!</p>
<p>Ceri Williams (Musical Director) superbly and expertly put together the musical accompaniment (supported by Margaret Kelt) and managed to command the total concentration and superb diction, so necessary for the understanding of G & S.</p>
<p>Adele Hodgson (Choreographer) provided the really clever Movement and Dance pieces, which enhanced the updated era. All the cast were obviously well drilled and didn’t put a head, hand or foot wrong.</p>
<p>There wasn’t one weak link in the casting of this show. Every member of the company were well suited to the individual characters, whether it was Principal, Supporting Roles, Ladies of the Village or Dragoons.</p>
<p>I feel that I must however make a special mention for Charlotte Rimmer (Patience) and also Phil Clieve ( Archibald) both had the audience eating out of their hands, with their excellent timing, accent and characterization. Both were extremely strong in voice (again suited to their Character), perfect diction (heard every word!) and not a flat note in sight! </p>
<p>The set was very well thought out, suited to the era and was very well adapted for the venue, also well done to Sound and Lighting both very well balanced and unobtrusive.</p>
<p>A special mention to Wardrobe. Clever! Really authentic, yet updated and just so "Hip and Glam"!!

<p>Finally I would like to say a final “Well Done” to all concerned, Committee, Production Team, Cast, Crew, and not forgetting Front of House! For another really fabulous Production in this you’re 60th Year!!</p>
I would like to give a big thanks to Andrew Lamb (Chairman) for the warm welcome and kind hospitality.
<h4>Wendy Newton. NODA.</h4>
