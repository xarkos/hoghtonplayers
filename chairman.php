                 <h2>Chairman's Address</h2><br>

                <p>Twenty years ago, Mr John Hilton asked me to be Musical Director for ‘The Mikado’. Since then I have been very much involved with this wonderful, friendly society.</p>

<p>In latter years I have mainly been involved as an accompanist and it is a real pleasure to play for such musically gifted singers. In our October concerts of HMS Pinafore several of the solo parts were double cast in order to accommodate our many talented members and, thanks to the skill and hard work of Mr Ian Dunning, all concerts were a resounding success. As always, Mr John Hilton’s narration surpassed the libretto!</p>

<p>At this year’s AGM it was a really special privilege for me to present Tom Mackley with the Iris Aughton bursary. I have seen Tom grow up with the society and he has been involved in at least 8 productions. Tom, having attained his degree in physics, is now studying music and drama at the Scottish Conservatoire.</p>

<p>Our society had two particularly special events to sing for in 2012: Ian and Diane Mackley celebrated their silver wedding by renewing their wedding vows and Adele    and Philip Clieve were married. Both couples met each other at G & S rehearsals!</p>

<p>Our forthcoming production of ‘Yeomen of the Guard’ is at the Richard Colmer theatre in Southport College. Mr John Hilton is the producer, Mrs Adele Clieve the choreographer and I am privileged to be the musical director with the invaluable help of Mr Ceri Williams.</p>

<p>Unusually in the G & S operettas, ‘Yeomen of the Guard’ has a sad ending. I too will be sad at the end of my chairmanship.  My husband and I are moving to Scotland so my regular contact with the society will come to an end.</p>

<p>I have had many wonderful times with this society and would urge any would be singers looking at this web-site to come along to Scarisbrick Road Baptist Church Hall on a Monday evening to sample the really high standard of singing and enjoy the very friendly welcome you are sure to get. Rehearsals are 7.30pm to 9.30pm</p>

<p>Last, but certainly not least, I would like to thank Mr Andrew Lamb for his excellent chairmanship last year, and congratulate Mr Dennis Davies who will be chairman next year.</p>

<h4>Margaret Kelt</h4>



