                      <br><br>
                       <p>In the Cornish fishing village of Rederring the professional bridesmaids are desperate for a wedding.  Hannah explains that, because of the curse of the Murgatroyds, she cannot marry; Rose Maybud, the village belle, is waiting for Robin Oakapple to propose, but he is shy and says nothing!  Robin’s shyness is partly because, although he is masquerading as a young farmer, he is really Sir Ruthven Murgatroyd, the bad baronet of Ruddigore, who has fled his inheritance to avoid having to commit a crime a day or perish in inconceivable agonies!</p>
 
 <p>The plot (as if not already thick enough!) thickens with the arrival of the breezy sailor, Dick Dauntless, Robin’s foster brother, who agrees to woo Rose on Robin’s behalf.  Richard, however, on seeing the blooming Rose, decides to have her for himself.  There follows some trading off between Richard, Robin and Rose (they are all rather selfish in their ways) but Rose finally realizes that Robin is the better catch in material terms (she is quite a little gold-digger!).  There now appears Mad Margaret, made mad by her rejection by yet another Murgatroyd, Sir Despard.  She wanders aimlessly about the countryside, has heard of Rose Maybud and come to find her.</p>
 
 <p>Meanwhile, Richard, piqued by Rose’s rejection, snitches to Robin’s younger brother, Despard (who inherited the curse when Robin fled, and who he believes to be dead) that Robin is still alive.  The first act ends with Despard interrupting Rose and Robin’s wedding by unmasking the latter as the true bad baronet.  Robin must now become the bad baronet, Despard is free to marry Margaret and Rose has to make do with Richard because he’s the only one left!</p>
 
 <p>Act Two opens with Robin and his faithful servant in the hall of Ruddigore Castle trying to think of crimes to commit.  They are interrupted by the arrival of Rose and Richard, who have come to ask Robin’s consent to their marriage.  He agrees reluctantly and there now comes the big scene of the opera when the ghostly ancestors step down from their picture frames and enquire into the crimes Robin is committing, which they consider unsatisfactory.  The last ancestor to die, Sir Roderic, gives Robin a commission, which he must carry out in order to avoid death. </p>
 
 <p>Despard and Margaret now enter, having become a very respectable married couple, though Margaret has flashes of her old madness.  They implore Robin to reject his evil ways.  After Robin’s commission has gone somewhat awry there is a little more to-ing and fro-ing before we get the usual topsy-turvy Gilbertian solution and all, as it should in comic opera, ends happily!</p>

